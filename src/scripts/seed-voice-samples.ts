async function main(): Promise<void> {
  const app = await NestFactory.createApplicationContext(SeedModule, {
    logger: ['error', 'warn', 'log'],
  });

  const couch = app.get(CouchDBSyncService);
  const agent = app.get(ContentAgentService);
  const today = new Date().toISOString().split('T')[0];

  // Write samples directly (stable ids => re-runnable, no duplicates) instead
  // of saveVoiceSample(), which would fire 13 concurrent profile rebuilds.
  let i = 0;
  for (const post of POSTS) {
    i += 1;
    const id = `voice-samples/seed-${String(i).padStart(2, '0')}.md`;
    const markdown = `---\ntype: voice-sample\ncreated: ${today}\n---\n\n${post.trim()}\n`;
    await couch.writeFile(id, markdown);
    console.log(`[${i}/${POSTS.length}] ${id}`);
  }

  console.log('Rebuilding voice profile from samples...');
  await agent.refreshVoiceProfile();
  console.log('Done.');

  await app.close();
  process.exit(0);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
